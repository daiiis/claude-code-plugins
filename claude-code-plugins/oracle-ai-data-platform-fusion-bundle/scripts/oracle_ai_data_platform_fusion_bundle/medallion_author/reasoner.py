"""Pure-Python candidate scoring + incremental-risk classification.

The LLM-driven reasoning in the skill chat loop calls these primitives
to:

1. Rank ``observedBronzeSchema`` columns against a failed VP — produce
   a top-K list of :class:`CandidateProposal` with confidence labels.
2. Classify the incremental risk of a proposed candidate vs. the
   prior-pinned value (when refreshing).

The reasoner is deterministic: same inputs → same outputs. The LLM
adds *judgment* (reasoning paragraphs, prompt-driven candidate
shortlisting) on top of these deterministic primitives.
"""

from __future__ import annotations

import difflib
from dataclasses import dataclass
from typing import Literal

from ..schema.diagnostic_artifact import ObservedColumn, VariationPointFailure
from ..schema.incremental_impact import RiskLabel
from .knowledge_base import KnowledgeBase, KnownDelta, column_matches_pattern


Confidence = Literal["high", "medium", "low"]
"""Per-proposal confidence label surfaced to the operator.

* ``high`` — KB hint matches OR strong name-similarity to existing
  candidates.
* ``medium`` — moderate name similarity or weak KB hint.
* ``low`` — observed column doesn't obviously match; surface anyway
  so the operator sees the alternatives.
"""


@dataclass(frozen=True)
class CandidateProposal:
    """One ranked candidate the skill proposes to the operator.

    Operator review gates the proposal; the skill never auto-commits.
    """

    candidate: str
    """The observed column name the skill proposes adding to the VP's
    candidate list."""

    confidence: Confidence
    score: float
    """Numeric score in ``[0.0, 1.0]`` — higher is better. Used for
    sorting; surfaced as a tiebreaker to the LLM when it presents
    candidates."""

    kb_hint_id: str | None = None
    """ID of the matching :class:`KnownDelta`, when the proposal is
    KB-driven. Threaded into the LLM's reasoning paragraph."""

    rationale: str = ""
    """One-paragraph operator-facing explanation. Generated by the LLM
    based on the deterministic score + the KB hint; this field is
    empty in the deterministic output and populated by the chat loop."""


# ---------------------------------------------------------------------------
# Candidate scoring
# ---------------------------------------------------------------------------


def score_candidates(
    *,
    failed_vp: VariationPointFailure,
    observed: list[ObservedColumn],
    kb: KnowledgeBase,
    top_k: int = 3,
) -> list[CandidateProposal]:
    """Score every observed column for the failed VP; return top K.

    Algorithm:

    1. Filter ``observed`` to columns NOT already in
       ``failed_vp.candidates_tried`` (those were rejected by the
       walker — proposing them again wouldn't help).
    2. For each remaining column:
       a. Compute name-similarity score against the candidates the
          walker DID try (these are the pack-author's reference
          candidates).
       b. Compute KB-hint bonus: if any :class:`KnownDelta` matching
          this VP has a pattern that hits the column, add a bonus.
    3. Sort by total score descending; return the top K with
       confidence labels.

    Args:
        failed_vp: the parsed ``VariationPointFailure`` from the
            diagnostic artifact.
        observed: ``failed_vp.observed_bronze_schema`` — passed
            separately so tests can drive without mocking the artifact.
        kb: loaded :class:`KnowledgeBase`.
        top_k: number of proposals to surface.

    Returns:
        Up to ``top_k`` :class:`CandidateProposal` records, sorted by
        score descending. Empty when no observed columns are
        plausible candidates.
    """
    tried = {a.candidate.lower() for a in failed_vp.candidates_tried}
    kb_deltas = kb.for_variation_point(name=failed_vp.name, kind=failed_vp.kind)

    proposals: list[CandidateProposal] = []
    for col in observed:
        if col.name.lower() in tried:
            continue
        score, kb_hint_id = _score_column(
            column_name=col.name,
            tried_candidates=[a.candidate for a in failed_vp.candidates_tried],
            kb_deltas=kb_deltas,
        )
        if score <= 0.0:
            continue
        proposals.append(
            CandidateProposal(
                candidate=col.name,
                confidence=_confidence_label(score),
                score=score,
                kb_hint_id=kb_hint_id,
            )
        )

    proposals.sort(key=lambda p: p.score, reverse=True)
    return proposals[:top_k]


def _score_column(
    *,
    column_name: str,
    tried_candidates: list[str],
    kb_deltas: list[KnownDelta],
) -> tuple[float, str | None]:
    """Score a single observed column against the VP context.

    Returns ``(score, kb_hint_id)``. Score is the higher of:

    * Name-similarity to the closest tried candidate (using
      :func:`difflib.SequenceMatcher.ratio`).
    * KB-hint bonus (0.85 for a high-priority match, 0.6 for medium,
      0.4 for low).

    A column matching a KB pattern dominates over name similarity to a
    weak candidate — the KB encodes operator-validated patterns.
    """
    similarity_score = 0.0
    for candidate in tried_candidates:
        ratio = difflib.SequenceMatcher(
            None, column_name.lower(), candidate.lower()
        ).ratio()
        similarity_score = max(similarity_score, ratio)

    kb_score = 0.0
    kb_hint_id: str | None = None
    for delta in kb_deltas:
        if not column_matches_pattern(column_name, delta.pattern):
            continue
        delta_score = {
            "high": 0.85,
            "medium": 0.6,
            "low": 0.4,
        }.get(delta.priority, 0.5)
        if delta_score > kb_score:
            kb_score = delta_score
            kb_hint_id = delta.id

    return max(similarity_score, kb_score), kb_hint_id


def _confidence_label(score: float) -> Confidence:
    if score >= 0.75:
        return "high"
    if score >= 0.5:
        return "medium"
    return "low"


# ---------------------------------------------------------------------------
# Incremental-risk classification
# ---------------------------------------------------------------------------


def classify_incremental_risk(
    *,
    prior_pinned: str | None,
    proposed: str,
    kb: KnowledgeBase,
    vp_name: str,
    kind: str,
) -> RiskLabel:
    """Classify whether a candidate promotion is rename-only vs. data drift.

    Heuristic:

    * No ``prior_pinned`` → initial onboarding → no incremental risk →
      effectively ``likely-rename`` (operator confirms; nothing to
      back-fill).
    * Name-similarity ratio ≥ 0.7 AND a KB delta marks the pair as a
      Fusion rename → ``likely-rename``.
    * KB delta with high priority on the proposed column AND the
      delta's notes mention "rename" → ``likely-rename``.
    * Otherwise the names suggest different data → ``likely-different-semantics``.
    * When both names are too short to compare meaningfully →
      ``unknown``.
    """
    if prior_pinned is None or not prior_pinned:
        return "likely-rename"

    if len(proposed) < 3 or len(prior_pinned) < 3:
        return "unknown"

    ratio = difflib.SequenceMatcher(None, prior_pinned.lower(), proposed.lower()).ratio()
    kb_deltas = kb.for_variation_point(name=vp_name, kind=kind)
    kb_hit = any(
        column_matches_pattern(proposed, d.pattern)
        and column_matches_pattern(prior_pinned, d.pattern)
        for d in kb_deltas
    )

    if ratio >= 0.7 and kb_hit:
        return "likely-rename"
    # KB hit alone but lower similarity → still likely rename; KB
    # encodes the operator-validated equivalence.
    if kb_hit and any(
        "rename" in d.notes.lower() or "renamed" in d.notes.lower()
        for d in kb_deltas
    ):
        return "likely-rename"
    if ratio >= 0.7:
        # Names are very close even without a KB hit — treat as
        # likely-rename but the LLM should surface the uncertainty.
        return "likely-rename"
    if ratio < 0.4:
        return "likely-different-semantics"
    return "unknown"


